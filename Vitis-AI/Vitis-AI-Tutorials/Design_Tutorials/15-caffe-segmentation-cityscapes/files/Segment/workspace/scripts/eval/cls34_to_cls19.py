def get_all_paths(img_dir, suffix):
    img_list = []
    for root, dirs, files in os.walk(img_dir):
        for name in files:
            if name.find(suffix) >= 0:
                img_list.append(os.path.join(root, name))

    return img_list


def cls34_cls19(img_path, dst_path, color = False):

    final_dir, _ = os.path.split(dst_path)
    if not os.path.exists(final_dir):
        os.makedirs(final_dir)

    print(dst_path)
    img = cv2.imread(img_path,0) # gray
    H, W = img.shape
    res = img.copy()
    # test
    if color:
        res = np.zeros((H,W,3))
    for w in range(W):
        for h in range(H):
            if color:
                res[h,w,:] = id2color[img[h,w]]
            else:
                res[h, w] = id2trainId[img[h, w]]
    cv2.imwrite(dst_path, res)

